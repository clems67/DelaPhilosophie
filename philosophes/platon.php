			<div class="titre"><strong>Introduction</strong></div>
			Platon, philosophe grec, a laissé une œuvre philosophique considérable, d'où nous retiendrons essentiellement les dialogues de jeunesse : Hippias majeur, Protagoras, Gorgias, etc. ; les dialogues de la maturité : Phédon, Banquet, Phèdre, République ; les dialogues de la vieillesse : Parménide, Théétète, Sophiste, Politique, Philèbe, Les Lois.
			<br>
			Le monde sensible est, aux yeux de Platon, subordonné aux Essences ou Idées, formes intelligibles, modèles de toutes choses, qui sauvent les phénomènes et leur donnent sens. Au sommet des Essences se trouve l'Idée du Bien, qui les dépasse en dignité et en puissance : ce principe suprême se confond avec le Divin.
			<br> <br> <br>
			<strong>Philosophie</strong> <br>
			<div class="titre">1. Le dialogue platonicien</div> 
			Platon nous a généralement présenté sa doctrine sous une forme particulière, celle du dialogue. Socrate, le maître du philosophe, incarnant en quelque sorte le « meneur de jeu ». En rédigeant son œuvre de manière dialoguée, Platon a ainsi mis en évidence une dimension importante inhérente à la recherche du vrai : elle s'effectue à deux (ou à plusieurs) et cette médiation est seule en mesure de nous faire dépasser les opinions particulières pour nous faire accéder à l'Universel. Qu'est-ce, en effet, qu'un dialogue ? Quand deux individus sont face à face et que deux types de croyances s'affrontent, en un débat organisé, cette discussion porte le nom de dialogue.
			<br> Mais le dialogue lui-même est un aspect rudimentaire de la dialectique, qu'il faut maintenant définir et décrire.
